"""
Tipo Booleano

Álgebra Booleana, criada por George Boole

2 constantes, Verdadeiro ou Falso

True -> Verdadeiro
False -> Falso

Obs: Sempre com inical maiúscula

"""

usuario = True

print(usuario)

"""
# Negação (not): negação
"""
print(not usuario)

"""
# Ou (or): Pelo menos um verdadeiro
"""
print(True or False)

"""
# E (and):Os dois precisa ser verdadeiro
"""
print(True and False)